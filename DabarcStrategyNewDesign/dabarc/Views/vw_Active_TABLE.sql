CREATE VIEW dabarc.vw_Active_TABLE
AS
SELECT     table_id, name, short_description, [priority], 'TABLE' Type, 'TFF' Type_Table, database_id, 0 tdm_id
FROM         t_TFF
WHERE     registered = 1 AND active = 1
UNION
SELECT     table_id, name, short_description, [priority], 'TABLE' Type, 'TDM' Type_Table, database_id, 0 tdm_id
FROM         t_TDM
WHERE     registered = 1 AND active = 1
UNION
SELECT     tfm.table_id, tfm.name, tfm.short_description, tfm.priority, 'TABLE' Type, 'TFM' Type_Table, tdm.database_id, tdm_id
FROM        dabarc.t_TFM tfm
INNER JOIN dabarc.t_TDM tdm
ON tfm.tdm_id = tdm.table_id
WHERE     TFM.registered = 1 AND TFM.active = 1

GO
